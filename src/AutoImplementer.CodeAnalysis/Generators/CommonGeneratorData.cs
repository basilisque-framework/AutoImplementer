using System.Reflection;

namespace Basilisque.AutoImplementer.CodeAnalysis.Generators;

/// <summary>
/// Provides data necessary for generating source code
/// </summary>
public static class CommonGeneratorData
{
    internal const string AutoImplementedAttributesTargetNamespace = "Basilisque.AutoImplementer.Annotations";

    internal static readonly AssemblyName AssemblyName = Assembly.GetExecutingAssembly().GetName();

    /// <summary>
    /// Provides a generic file header for generated code files
    /// </summary>
    public static readonly string GeneratedFileSharedHeader = @$"//------------------------------------------------------------------------
// <auto-generated>
//   This code was generated by a tool.
//   {AssemblyName.Name}, {AssemblyName.Version}
//   
//   Changes to this file may cause incorrect behavior and will be lost if
//   the code is regenerated.
// </auto-generated>
//------------------------------------------------------------------------";

    /// <summary>
    /// Provides a generic file header for generated code files including a nullable enable directive
    /// </summary>
    public static readonly string GeneratedFileSharedHeaderWithNullable = @$"{GeneratedFileSharedHeader}

#nullable enable
";

    /// <summary>
    /// Provides a generic file header for generated code files including some common usings
    /// </summary>
    public static readonly string GeneratedFileSharedHeaderWithUsings = $@"{CommonGeneratorData.GeneratedFileSharedHeaderWithNullable}

using System;";

    /// <summary>
    /// Provides the attributes that are output for each class as a string
    /// </summary>
    public static readonly string GeneratedClassSharedAttributes = $@"[global::System.CodeDom.Compiler.GeneratedCodeAttribute(""{CommonGeneratorData.AssemblyName.Name}"", ""{CommonGeneratorData.AssemblyName.Version}"")]
    [global::System.Diagnostics.DebuggerNonUserCodeAttribute()]
    [global::System.Runtime.CompilerServices.CompilerGeneratedAttribute()]";

    /// <summary>
    /// Provides the attributes that are output for each class as a string (not indented)
    /// </summary>
    public static readonly string GeneratedClassSharedAttributesNotIndented = GeneratedClassSharedAttributes.Replace("    [", "[");
}